using Microsoft.EntityFrameworkCore;
using MostAPI;
using MostAPI.Data;
using System;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
builder.Services.AddControllers();
string databaseUrl = Environment.GetEnvironmentVariable("DATABASE_URL") ?? throw new InvalidOperationException("DATABASE_URL not set");
builder.Configuration["ConnectionStrings:DefaultConnection"] = DatabaseHelper.ConvertPostgresUrlToConnectionString(databaseUrl);

// Добавляем сервисы
builder.Services.AddDbContext<ApplicationDbContext>(options =>
    options.UseNpgsql(builder.Configuration.GetConnectionString("DefaultConnection")));
// Добавляем настройку CORS
builder.Services.AddCors(options =>
{
    options.AddPolicy("AllowAll", policy =>
    {
        policy.AllowAnyOrigin()    // Разрешаем доступ с любого источника
              .AllowAnyMethod()    // Разрешаем любые методы HTTP (GET, POST и т.д.)
              .AllowAnyHeader();   // Разрешаем любые заголовки
    });
});

// Настройка Swagger (если она уже есть)
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
// Устанавливаем порт для запуска приложения на Render
var port = Environment.GetEnvironmentVariable("PORT") ?? "10000";
builder.WebHost.UseUrls($"http://*:{port}");

var app = builder.Build();



// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment() || app.Environment.IsProduction())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

// Включаем CORS для всех маршрутов
app.UseCors("AllowAll");

app.UseAuthorization();

app.MapControllers();

app.Run();
